    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);
        Toolbar toolbar = (Toolbar) findViewById(R.id.toolbar);
        setSupportActionBar(toolbar);

        getSupportLoaderManager().initLoader(FORECAST_LOADER, null, this);

//        Cursor cur = this.getContentResolver().query(weatherForLocationUri,
//                null, null, null, sortOrder);

        // The CursorAdapter will take data from our cursor and populate the ListView
        // However, we cannot use FLAG_AUTO_REQUERY since it is deprecated, so we will end
        // up with an empty list the first time we run.
        mForecastAdapter = new ForecastAdapter(this, null, 0);
        mLocation = Utility.getPreferredLocation(this);

        listView = (ListView) findViewById(R.id.listview_forecast);
        listView.setAdapter(mForecastAdapter);

        listView.setOnItemClickListener(new AdapterView.OnItemClickListener() {
            @Override
            public void onItemClick(AdapterView<?> adapterView, View view, int position, long id) {
                // CursorAdapter returns a cursor at the correct position for getItem(), or null
                // if it cannot seek to that position.
                Cursor cursor = (Cursor) adapterView.getItemAtPosition(position);
                String locationSetting = Utility.getPreferredLocation(getBaseContext());
                Uri rowUri = WeatherContract.WeatherEntry.buildWeatherLocationWithDate
                        (locationSetting, cursor.getLong(MainActivity.COL_WEATHER_DATE));

                Log.d("MainActivity", "rowUri: " + rowUri.getEncodedPath());

                startActivity(new Intent(getBaseContext(), DetailActivity.class).setData(rowUri));
            }
        });

    }

    @Override
    protected void onResume() {
        super.onResume();
        String location = Utility.getPreferredLocation( this );
        // update the location in our second pane using the fragment manager
        if (location != null && !location.equals(mLocation)) {
            onLocationChanged();
            mLocation = location;
        }
    }

    // since we read the location when we create the loader, all we need to do is restart things
    private void onLocationChanged( ) {
        getUpdatedWeatherInfo();
        getSupportLoaderManager().restartLoader(FORECAST_LOADER, null, this);
    }

